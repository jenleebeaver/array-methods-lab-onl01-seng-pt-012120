def using_include(array, element)
  return array.include?(element)
  return array
end

def using_sort(array)
  array.sort
  return array
end

def using_reverse(array)
  array.reverse
  return array
end

def using_first(array)
  array.first
end

def using_last(array)
  array.last
end

def using_size(array)
  array.size
end
